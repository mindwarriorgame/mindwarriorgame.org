<div id="smooth-pause" class="faq-item">
    <h3>How to take a break without penalties?</h3>

    <p>
        To take a break, simply pause the game with ⏸️ <span class="pre">/pause</span> command.
    </p>

    <p>
        During the pause, you won't receive any notifications or lose any gaming scores.
        The active play time counter won't be counting, too.
    </p>

    <p>
        To resume the game, simply review your <i>Letter</i> as usually with
        💫 <span class="pre">/review</span> command.
    </p>
