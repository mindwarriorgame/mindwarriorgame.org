<div id="smooth-forgot" class="faq-item">
    <h3>What happens if I forget to review my Letter?</h3>

    <p>
        If you forget to review your <i>Letter</i> in time, the game will remind you with a message,
        but you will lose a gaming score 📉.
    </p>

    <p>
        <a href="/images/faq_en/review_minus_score.png" class="image-link" target="_blank>"><img src="/images/faq_en/review_minus_score.png" alt="Review minus score" title="Review minus score" width="400" /></a>
    </p>

    <p>
        For the first missed review, you will lose only 3 points (lenient mode), however for each consequent
        miss you will be penalized in full
        with 6 points, until you finish the review.
    </p>
</div>

